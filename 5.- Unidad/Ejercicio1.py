#Ejercicio 1: Modulos

def suma(*args):
	num = 0
	for n in args:
		suma += n

	return num

def resta(*args):
	num = 0
	for n in args:
		num -= n

	return num

def producto(*args):
	num = 1
	for n in args:
		num *= n

	return num

def division(*args):
	num = 1
	for n in args:
		num /= n

	return num

 
