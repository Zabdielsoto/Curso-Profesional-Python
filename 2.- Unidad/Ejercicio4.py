'''Ejercicio 4: Cadenas
4.- Utilizando métodos de cadenas, 
identificar cuál de los siguientes 
caracteres son ascii: ñ, d, s, a, $, &.
'''

if 'ñ'.isascii() == True:
	print("'ñ' es un caracter ASCII\n")
else:
	print("'ñ' NO es un caracter ASCII\n")

if 'd'.isascii() == True:
	print("'d' es un caracter ASCII\n")
else:
	print("'d' NO es un caracter ASCII\n")

if 's'.isascii() == True:
	print("'s' es un caracter ASCII\n")
else:
	print("'s' NO es un caracter ASCII\n")

if 'a'.isascii() == True:
	print("'a' es un caracter ASCII\n")
else:
	print("'a' NO es un caracter ASCII\n")

if '$'.isascii() == True:
	print("'$' es un caracter ASCII\n")
else:
	print("'$' NO es un caracter ASCII\n")

if '&'.isascii() == True:
	print("'&' es un caracter ASCII\n")
else:
	print("'&' NO es un caracter ASCII\n")